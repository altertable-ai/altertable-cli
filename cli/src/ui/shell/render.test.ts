import { describe, expect, test } from "bun:test";
import { renderShellExportView } from "@/ui/shell/render.ts";

describe("renderShellExportView", () => {
  test("renders comments followed by quoted environment exports", () => {
    expect(
      renderShellExportView({
        env: { ALTERTABLE_PROFILE: "acme_prod" },
        comments: ["Generated by: altertable profile env"],
      }),
    ).toBe('# Generated by: altertable profile env\nexport ALTERTABLE_PROFILE="acme_prod"');
  });
});
